#include<stdio.h>
void main()
{
// arithmetic operators include +,-,*,/,%
int a,b;
printf("enter any two values for a and b\n");
scanf("%d%d",&a,&b);
printf("value of a+b is %d\n",a+b);
printf("value of a-b is %d\n",a-b);
printf("value of a*b is %d\n",a*b);
printf("value of a/b is %d\n",a/b);
printf("value of a%b is %d\n",a%b);
}


